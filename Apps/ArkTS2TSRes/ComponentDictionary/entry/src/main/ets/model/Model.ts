//组件数据模型
export class ComponentItem {
    superId: number; //分类id
    superName: string; //分类标题
    id: number; // 组件id
    componentName: string; // 组件名称
    constructor(superId: number, superName: string, id: number, componentName: string) {
        this.superId = superId;
        this.superName = superName;
        this.id = id;
        this.componentName = componentName;
    }
}
//预置数据
export function initializeOnStartup(): Array<ComponentItem> {
    //1. 创建一个名为componentArray的空数组，数组类型应定义为数据模型类，该数组用于转存填充的数据
    let componentArray: Array<ComponentItem> = [];
    //2. 遍历存储了组件原始信息的数组ComponentData
    ComponentData.forEach((item) => {
        //3.将ComponentData数组中的数据一一转存到我们创建的ComponentItem类型数组中
        componentArray.push(new ComponentItem(item.superId, item.superName, item.id, item.componentName));
    });
    //页面即可直接从该componentArray数组中获取需要的数据
    return componentArray;
}
